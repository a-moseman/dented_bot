package Game.Item;

public class AttributeModifiers {
    public final int STR;
    public final int DEX;
    public final int CON;
    public final int INT;
    public final int WIS;
    public final int CHA;
    
    public AttributeModifiers(int STR, int DEX, int CON, int INT, int WIS, int CHA) {
        this.STR = STR;
        this.DEX = DEX;
        this.CON = CON;
        this.INT = INT;
        this.WIS = WIS;
        this.CHA = CHA;
    }
}
